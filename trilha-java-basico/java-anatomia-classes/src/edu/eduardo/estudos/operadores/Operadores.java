package edu.eduardo.estudos.operadores;

public class Operadores {
    public static void main(String[] args) {
        int numero = 5;

        // Converte numero para negativo;
        numero = - numero;

        System.out.println(numero);

        // Converte numero para positivo;
        numero = numero * -1;

        System.out.println(numero);
    }
}
